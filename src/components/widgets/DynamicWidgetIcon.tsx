interface DynamicWidgetIconProps {
  iconValue: string; // Nombre Lucide (ej. "Star") o data: URL (imagen)
  className?: string;
}

export function DynamicWidgetIcon({ iconValue, className }: DynamicWidgetIconProps) {
  if (!iconValue) {
    return <FolderOpen className={cn("w-4 h-4", className)} />;
  }

  // Si es una data URL, renderizar como imagen
  if (iconValue.startsWith("data:")) {
    return (
      <img
        src={iconValue}
        className={cn("w-4 h-4 object-contain rounded-sm", className)}
        alt=""
        draggable={false}
      />
    );
  }

  // Buscar en el mapa estático de iconos
  const IconComponent = ICON_MAP[iconValue];
  if (IconComponent) {
    return <IconComponent className={cn("w-4 h-4", className)} />;
  }

  // Fallback
  return <FolderOpen className={cn("w-4 h-4", className)} />;
}
