// Sort keys alphabetically
const sortObj = (obj) => {
  const sorted = {};
  for (const key of Object.keys(obj).sort()) {
    sorted[key] = obj[key];
  }
  return sorted;
};

const sortedEs = sortObj(esData);
const sortedEn = sortObj(enData);

// Write back
writeFileSync(esPath, JSON.stringify(sortedEs, null, 2) + '\n', 'utf-8');
